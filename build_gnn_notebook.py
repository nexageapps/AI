# === SECTION 5: GNNs ===
md([
"---\n",
"<a id='5'></a>\n",
"## 5. Graph Neural Networks (GNNs)\n",
"\n",
"### Definition\n",
"\n",
"> A GNN is an **optimisable transformation** on all attributes of the graph (nodes, edges)\n",
"> that **preserves graph symmetries** (permutation invariances).\n",
"\n",
"### Key Properties\n",
"\n",
"- Takes adjacency matrix $\\mathbf{A}$ and node feature matrix $\\mathbf{X}$ as input\n",
"- Node embeddings are updated after each layer to create hidden representations $\\mathbf{H}_k$\n",
"- Each hidden layer outputs updated node representations that include information about\n",
"  the node **itself** and its **context** within the graph\n",
"- Similar to word embeddings passing through a transformer: outputs include sentence context\n",
"\n",
"### Main Idea: Message Passing\n",
"\n",
"1. **Pass messages** between pairs of connected nodes\n",
"2. **Aggregate** messages at each node\n",
"3. **Update** node representations based on aggregated messages\n",
"\n",
"This refines node (and possibly edge) representations layer by layer.\n",
"\n",
"### Notation\n",
"\n",
"- Graph: $\\mathcal{G} = (\\mathbf{A}, \\mathbf{X})$\n",
"- Adjacency matrix: $\\mathbf{A} \\in \\mathbb{R}^{N \\times N}$\n",
"- Feature matrix: $\\mathbf{X} \\in \\mathbb{R}^{N \\times F}$\n",
"- Hidden state at layer $k$: $\\mathbf{H}_k$\n",
"- Flow: $\\mathbf{A}, \\mathbf{X} \\longrightarrow \\mathbf{H}_1 \\longrightarrow \\mathbf{H}_2 \\longrightarrow \\cdots \\longrightarrow \\mathbf{H}_K$\n",
"\n",
"### Why Not Use Classic MLPs?\n",
"\n",
"Classic MLPs are **not** permutation invariant/equivariant:\n",
"- Switching the order of input nodes leads to **different outputs**\n",
"- We need specialised architectures that respect graph structure!"
])


# === SECTION 6: GCNs ===
md([
"---\n",
"<a id='6'></a>\n",
"## 6. Graph Convolutional Networks (GCNs)\n",
"\n",
"### Overview\n",
"\n",
"A **GCN** is one type of GNN that updates each node by **aggregating information from\n",
"nearby nodes**.\n",
"\n",
"- Induces a **relational inductive bias**: prioritises information from neighbors\n",
"- Intuition: Nodes aggregate information from their neighbors using neural networks\n",
"\n",
"### Simple GCN Layer Formulation\n",
"\n",
"$$\\mathbf{h}_{k+1}^{(n)} = \\mathbf{a}\\left[\\boldsymbol{\\beta}_k + \\boldsymbol{\\Omega}_k \\mathbf{h}_k^{(n)} + \\boldsymbol{\\Omega}_k \\text{agg}[n]\\right]$$\n",
"\n",
"Where:\n",
"- $\\mathbf{h}_{k+1}^{(n)}$ = hidden representation for node $n$ at layer $k+1$\n",
"- $\\mathbf{h}_k^{(n)}$ = hidden representation for node $n$ at layer $k$\n",
"- $\\boldsymbol{\\beta}_k$ = bias term at layer $k$ (learnable)\n",
"- $\\boldsymbol{\\Omega}_k$ = weight matrix at layer $k$ (learnable)\n",
"- $\\text{agg}[n]$ = aggregation of neighbor representations\n",
"- $\\mathbf{a}[\\cdot]$ = non-linear activation (e.g., ReLU)\n",
"\n",
"### Key Design Choices\n",
"\n",
"**Parameters are shared across nodes:**\n",
"- Reduces the number of parameters\n",
"- Shares what the network learns at each node across the entire graph\n",
"\n",
"**Aggregation functions** (must be permutation invariant):\n",
"- Sum\n",
"- Average (mean)\n",
"- Max pooling\n",
"- Attention-weighted aggregation\n",
"\n",
"### Kipf & Welling GCN Layer (2016)\n",
"\n",
"The most well-known GCN formulation:\n",
"\n",
"$$\\mathbf{H}^{(l+1)} = \\sigma\\left(\\tilde{\\mathbf{D}}^{-1/2} \\tilde{\\mathbf{A}} \\tilde{\\mathbf{D}}^{-1/2} \\mathbf{H}^{(l)} \\mathbf{W}^{(l)}\\right)$$\n",
"\n",
"Where:\n",
"- $\\tilde{\\mathbf{A}} = \\mathbf{A} + \\mathbf{I}$ (adjacency with self-loops)\n",
"- $\\tilde{\\mathbf{D}}$ = degree matrix of $\\tilde{\\mathbf{A}}$\n",
"- $\\tilde{\\mathbf{D}}^{-1/2} \\tilde{\\mathbf{A}} \\tilde{\\mathbf{D}}^{-1/2}$ = symmetric normalisation\n",
"- $\\mathbf{H}^{(l)}$ = node embeddings before layer $l$\n",
"- $\\mathbf{W}^{(l)}$ = shared weight matrix at layer $l$\n",
"- $\\sigma$ = non-linearity (e.g., ReLU)"
])


