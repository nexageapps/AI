# === SECTION 7: PERMUTATION INVARIANCE/EQUIVARIANCE ===
md([
"---\n",
"<a id='7'></a>\n",
"## 7. Permutation Invariance and Equivariance\n",
"\n",
"Node indexing in graphs is **arbitrary** - permuting node indices does not change the\n",
"underlying graph (unlike pixels in images or words in text).\n",
"\n",
"Any processing applied to the graph should be **indifferent to index permutations**.\n",
"\n",
"### Permutation Invariance\n",
"\n",
"A function $f$ mapping a graph $G = (\\mathbf{A}, \\mathbf{X})$ to a $d$-dimensional embedding is\n",
"**permutation invariant** if:\n",
"\n",
"$$f(\\mathbf{A}_i, \\mathbf{X}_i) = f(\\mathbf{A}_j, \\mathbf{X}_j)$$\n",
"\n",
"for any orderings $i$ and $j$.\n",
"\n",
"\u2192 If you shuffle the nodes in the input, the overall **graph-level output** doesn't change.\n",
"\n",
"**Example:** Graph classification - the predicted class should be the same regardless of\n",
"how we number the nodes.\n",
"\n",
"### Permutation Equivariance\n",
"\n",
"A function $f$ mapping each node of $G$ to a $d$-dimensional embedding is\n",
"**permutation equivariant** if:\n",
"\n",
"$$f(\\mathbf{P}\\mathbf{A}\\mathbf{P}^\\top, \\mathbf{P}\\mathbf{X}) = \\mathbf{P} f(\\mathbf{A}, \\mathbf{X})$$\n",
"\n",
"for any permutation matrix $\\mathbf{P}$.\n",
"\n",
"\u2192 If you shuffle the nodes in the input, the **output node embeddings get shuffled in the same way**.\n",
"\n",
"**Example:** Node classification - each node's predicted class stays the same regardless of ordering.\n",
"\n",
"### GNNs and Permutation Properties\n",
"\n",
"Graph neural networks consist of multiple permutation equivariant/invariant functions:\n",
"- **GCN layers** are permutation equivariant (node embeddings follow the permutation)\n",
"- **Aggregation** (e.g., sum/mean over all nodes) is permutation invariant\n",
"- Combining equivariant layers + invariant readout = invariant graph-level predictions"
])


