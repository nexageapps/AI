# === SECTION 4: GRAPH REPRESENTATION ===
md([
"---\n",
"<a id='4'></a>\n",
"## 4. Graph Representation\n",
"\n",
"To apply deep learning to graphs, they need to be formally represented using matrices:\n",
"\n",
"### Adjacency Matrix $\\mathbf{A}$\n",
"\n",
"- Binary matrix of size $N \\times N$ (where $N$ = number of nodes)\n",
"- Element $(m, n)$ is 1 if node $m$ connects to node $n$, else 0\n",
"- **Symmetric** for undirected graphs, **asymmetric** for directed graphs\n",
"- Diagonal is 0 for simple graphs (no self-loops)\n",
"\n",
"### Node Data Matrix $\\mathbf{X}$\n",
"\n",
"- Matrix of size $D \\times N$ (or $N \\times F$ in some notations)\n",
"- Each column/row contains the **embedding** (feature vector) for one node\n",
"- $D$ (or $F$) = dimensionality of node features\n",
"\n",
"### Edge Data Matrix $\\mathbf{E}$\n",
"\n",
"- Matrix of size $D_E \\times E$ where $E$ = number of edges\n",
"- Each column contains the embedding for one edge\n",
"\n",
"### Adjacency Matrix as Context\n",
"\n",
"The adjacency matrix encodes structural information:\n",
"- $\\mathbf{A}$ contains the number of walks of length 1 between nodes\n",
"- $\\mathbf{A}^L$ contains the number of walks of length $L$ between nodes\n",
"- Pre-multiplying a node representation by $\\mathbf{A}$ gives information about\n",
"  the node's **local context** (its neighbors)"
])

